### sort 와 sorted 의 차이점
### python 정렬의 종류

### 1. sort와 sorted의 차이
"""
sort : 원본 데이터 정렬
a.sort
원본 리스트를 직접 수정
반환값이 None 없음
a = a.sort()
a=None
"""
a = [1,6,7,5,10,3]
a.sort()
print(a) ### 순서대로 정렬
# a=a.sort()
# print(a)

"""
sorted : 복사본을 만들어 정렬 (원본 유지)
리스트뿐만 아니라 튜플, 문자열, 딕셔너리 등 다른 반복 가능한 객체에도 사용 가능
result = sorted(a)
원본 데이터를 유지하고 정렬된 복사본을 반환

"""
a = [1, 6, 7, 5, 10, 3]
result = sorted(a)
print(result)


