### 정렬의 종류
"""
버블 정렬(Bubble Sort) : 인접한 두 요소를 비교하고 필요에 따라 교환하는 방식으로 정렬을 수행
선택 정렬(Selection Sort) : 리스트에서 가장 작은 요소를 찾아 정렬되지 않은 부분의 맨 앞에 위치시키는 방식
삽입 정렬(Insertion Sort) : 리스트를 두 부분으로 나누어 정렬되지 않은 부분에서 요소를 하나씩 꺼내 정렬된 부분에 삽입하는 방식
병합 정렬(Merge Sort) : 배열을 절반으로 나눈 후, 각각을 재귀적으로 병합 정렬하고 두 부분을 합치는 방식
퀵 정렬(Quick Sort) : 배열에서 하나의 피벗 요소를 선택하고, 이 피벗을 기준으로 작은 값은 왼쪽, 큰 값은 오른쪽에 분할한 후, 각각을 재귀적으로 정렬하는 방식
"""